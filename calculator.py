def addition(num_uno, num_dos):
	"""Returns the result of a addition"""
	return num_uno + num_dos

def subtraction(num_uno, num_dos):
	"""Returns the result of a substraction"""
	return num_uno - num_dos

def  multiplication(num_uno, num_dos):
	"""Returns the result of a multiplication"""
	return num_uno * num_dos

def division(num_uno, num_dos):
	"""Returns the result of a division"""
	return num_uno / num_dos